public class FL {
    private float targetProjectFunding;
    private float targetTeamExperience;
	
    public FL() {
    	targetProjectFunding = 0;
    	targetTeamExperience = 0;
    }
    
	public FL(float projectFunding, float teamExperience) {
		targetProjectFunding = projectFunding;
		targetTeamExperience = teamExperience;
	}
	
	
}
